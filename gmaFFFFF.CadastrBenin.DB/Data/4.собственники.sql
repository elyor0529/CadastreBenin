 					TransactionTypeNom	   nvarchar(50)		default N'Купля',
					TransactionDocumentId	int				 NULL,
					DebutDate				date				null,
					ParcelGUID				uniqueidentifier not null);
					
declare @tranDoc table	(TranGUID	uniqueidentifier not null,
						DocID		int				not null);	
							
insert into @tran (DebutDate,ParcelGUID)
	select dateadd(day, -Floor(Rand(checksum(newid()))*10000), cast(current_timestamp as date)) as DebutDate,  Obj
	from @SubjObj
	group by Obj;

merge into Juridique.TransactionDocuments
	using (select N'Договор купли-продажи' as TransactionDocumentTypeNom, 
