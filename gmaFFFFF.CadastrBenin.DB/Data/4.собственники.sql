			Floor(Rand(checksum(newid()))*10000) as NumDoc, 
			TranGUID
		   from @tran) as T
	on (1 > 2)
	when not matched 
		then insert (TransactionDocumentTypeNom, [Date], Num) values(TransactionDocumentTypeNom, DebutDate, NumDoc)
	output inserted.Id, T.TranGUID into @tranDoc(DocID,TranGUID); 

update T
	set TransactionDocumentId = td.DocID
from @tran as t join @tranDoc as td on td.TranGUID = t.TranGUID;

